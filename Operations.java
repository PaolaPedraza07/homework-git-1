public class Operations {
    public int add(int a, int b){
        return a+b;
    }
    public int sub(int a, int b){
        return a-b;
    }
    public int div(int a, int b){
        return a/b;
    }
    public int mult(int a, int b){
        return a*b;
    }
    public double sqrt(double a, double b){
        return Math.pow(a,b);
    }
}